package leetcode.linkedlist.easy;

import leetcode.node.type4.ListNode;

/**
 * @author 窦康泰
 * @date 2021/04/23
 */
public class MergeTwoSortedLists {
    public ListNode mergeTwoLists(ListNode l1, ListNode l2) {
        if (l1 == null) {
            return l2;
        } else if (l2 == null) {
            return l1;
        }
        ListNode p;
        if (l1.val < l2.val) {
            p = l1;
            l1 = l1.next;
        } else {
            p = l2;
            l2 = l2.next;
        }
        ListNode head = p;
        while (l1 != null && l2 != null) {
            if (l1.val < l2.val) {
                p.next = l1;
                p = p.next;
                l1 = l1.next;
            } else {
                p.next = l2;
                p = p.next;
                l2 = l2.next;
            }
        }
        while (l1 != null) {
            p.next = l1;
            p = p.next;
            l1 = l1.next;
        }
        while (l2 != null) {
            p.next = l2;
            p = p.next;
            l2 = l2.next;
        }
        return head;
    }
}
